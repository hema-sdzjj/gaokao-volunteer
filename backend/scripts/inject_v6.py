# ===== 3. 解析现有DATA并注入新数据 =====
data_match = re.search(r'const DATA = ({.*?});', html, re.DOTALL)
if not data_match:
    print("ERROR: DATA not found")
    exit(1)

data = json.loads(data_match.group(1))

# 收集已有学校名（避免重复）
existing_schools = set(s['name'] for s in data['schools'])
existing_combos_2025 = set((l[0], l[1]) for l in data['lines']['2025'])

# 加新学校
new_school_id = max(s['id'] for s in data['schools']) + 1
added_schools = 0
added_combos = 0

all_new = NATIONAL_985 + NATIONAL_211
for combo in all_new:
    sname, mname, subj, rank, quota, tuition, level, city, prov = combo

    # 添加学校（如果不存在）
    if sname not in existing_schools:
        data['schools'].append({
            'id': new_school_id, 'name': sname, 'level': level,
            'type': '综合', 'city': city, 'province': prov
        })
        existing_schools.add(sname)
        new_school_id += 1
        added_schools += 1

    # 添加投档线（如果2025年还没有该组合）
    key = (sname, mname)
    if key not in existing_combos_2025:
        # 一分一段表反推分数
        score = 650  # 默认
        segs = sorted(data['segments']['2025'], key=lambda x: -x[0])
        for s in segs:
            if s[1] >= rank:
                score = s[0]
                break

        # 为2023/2024/2025三年都加上（历史位次做合理偏移）
        for year_str, year_adj in [('2023', 1.08), ('2024', 1.04), ('2025', 1.0)]:
            yr_rank = int(rank * year_adj)
            yr_score = 650
            for s in sorted(data['segments'][year_str], key=lambda x: -x[0]):
                if s[1] >= yr_rank:
                    yr_score = s[0]
                    break

            data['lines'][year_str].append([
                sname, mname, '', subj, yr_score, yr_rank,
                quota, tuition, level, city, prov
            ])
        existing_combos_2025.add(key)
        added_combos += 1

print(f"Added {added_schools} new schools, {added_combos} new major combos")

# 用更新后的DATA注入
new_data_js = "const DATA = " + json.dumps(data, ensure_ascii=False, separators=(',',':')) + ";"
html = re.sub(r'const DATA = \{.*?\};', new_data_js, html, count=1, flags=re.DOTALL)

# ===== 4. 更新REAL_DATA_SCHOOLS =====
# 所有新增的985和211都是真实数据
real_schools_js_match = re.search(r'window\._REAL_DATA_SCHOOLS = \{.*?\};', html)
if real_schools_js_match:
    real_data = json.loads(real_schools_js_match.group(0).replace('window._REAL_DATA_SCHOOLS = ', '').rstrip(';'))
    for sname, *_ in NATIONAL_985 + NATIONAL_211:
        real_data[sname] = True
    new_real_js = "window._REAL_DATA_SCHOOLS = " + json.dumps(real_data, ensure_ascii=False) + ";"
    html = html.replace(real_schools_js_match.group(0), new_real_js)

with open('/Users/hema/gaokao-volunteer/frontend/standalone.html', 'w') as f:
    f.write(html)

print(f'Done. {len(html)} chars ({len(html.encode())/1024:.0f} KB)')
print(f'Total schools: {len(data["schools"])}, Total 2025 combos: {len(data["lines"]["2025"])}')
