def _rank_to_approx_score(rank: int, year: int) -> int:
    """根据位次反推大致分数"""
    # 一分一段表反查（近似）
    # 参考: 2025年 444分约30万名
    if rank <= 100:
        return 695 - int(rank * 0.05)
    elif rank <= 1000:
        return 680 - int((rank - 100) * 0.015)
    elif rank <= 5000:
        return 665 - int((rank - 1000) * 0.005)
    elif rank <= 20000:
        return 640 - int((rank - 5000) * 0.0018)
    elif rank <= 50000:
        return 595 - int((rank - 20000) * 0.0012)
    elif rank <= 100000:
        return 545 - int((rank - 50000) * 0.001)
    elif rank <= 200000:
        return 505 - int((rank - 100000) * 0.0008)
    elif rank <= 300000:
        return 460 - int((rank - 200000) * 0.0006)
    else:
        return max(150, 444 - int((rank - 300000) * 0.0005))


def _get_school_level(name: str) -> str:
    for sn, level, *_ in SCHOOLS_DATA:
        if sn == name:
            return level
    return "普通"


def _get_school_city(name: str) -> str:
    for sn, _, _, city, *_ in SCHOOLS_DATA:
        if sn == name:
            return city
    return ""


def _get_school_province(name: str) -> str:
    for sn, _, _, _, province, *_ in SCHOOLS_DATA:
        if sn == name:
            return province
    return ""


